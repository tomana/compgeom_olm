ofPoint ofApp::intersection_two_lines(ofPoint p1, ofPoint p2, ofPoint p3, ofPoint p4)
{
// Store the values for fast access and easy
// equations-to-code conversion
    double x1 = p1.x, x2 = p2.x, x3 = p3.x, x4 = p4.x;
    double y1 = p1.y, y2 = p2.y, y3 = p3.y, y4 = p4.y;

    double d = (x1 - x2) * (y3 - y4) - (y1 - y2) * (x3 - x4);
// If d is zero, there is no intersection
    if (d == 0) return ofPoint(NULL,NULL,NULL);

// Get the x and y
    double pre = (x1*y2 - y1*x2), post = (x3*y4 - y3*x4);
    double x = ( pre * (x3 - x4) - (x1 - x2) * post ) / d;
    double y = ( pre * (y3 - y4) - (y1 - y2) * post ) / d;

// Check if the x and y coordinates are within both lines
    if ( x < std::min(x1, x2) || x > std::max(x1, x2) ||
            x < std::min(x3, x4) || x > std::max(x3, x4) ) return ofPoint(NULL,NULL,NULL);
    if ( y < std::min(y1, y2) || y > std::max(y1, y2) ||
            y < std::min(y3, y4) || y > std::max(y3, y4) ) return ofPoint(NULL,NULL,NULL);

// Return the point of intersection
    ofPoint ret = ofPoint(x, y);
    return ret;
}
